package Day5;

public class Porter {
    String name;
    String surname;
    int age;
    int salary;
    Shift shift;

    public Porter() {
    }

    public Porter(String name, String surname, int age, int salary, Shift shift) {
        this.name = name;
        this.surname = surname;
        this.age = age;
        this.salary = salary;
        this.shift = shift;
    }



}
